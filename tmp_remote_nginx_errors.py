import paramiko

def check_nginx_errors():
    host = "167.71.193.34"
    user = "root"
    pw = "vazovai11"
    
    ssh = paramiko.SSHClient()
    ssh.set_missing_host_key_policy(paramiko.AutoAddPolicy())
    
    try:
        ssh.connect(host, username=user, password=pw, timeout=15)
        
        print("--- NGINX ERROR LOGS ---")
        stdin, stdout, stderr = ssh.exec_command("docker exec sovereign_v15_gateway tail -n 50 /var/log/nginx/error.log")
        print(stdout.read().decode())
        
        ssh.close()
    except Exception as e:
        print(f"Error: {e}")

if __name__ == "__main__":
    check_nginx_errors()
